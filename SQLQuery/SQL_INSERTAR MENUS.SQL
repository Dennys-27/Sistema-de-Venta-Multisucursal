
CREATE PROCEDURE SP_I_MENU_01
@ROL_ID INT
AS
BEGIN
	IF (SELECT COUNT(*) FROM TD_MENU WHERE ROL_ID = @ROL_ID) = 0
	BEGIN 
	INSERT INTO TD_MENU
	(MEN_ID,ROL_ID,MEND_PERMISO,FECH_CREA,EST)
	(SELECT MEN_ID, @ROL_ID,'No',GETDATE(),1 FROM TM_MENU WHERE EST = 1)
	END
END


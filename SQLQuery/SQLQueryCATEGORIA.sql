ALTER PROCEDURE SP_L_CATEGORIA_01
@SUC_ID INT
AS
BEGIN 
	SELECT * FROM [dbo].[TM_CATEGORIA]
	WHERE
	SUC_ID=@SUC_ID
	AND EST=1
END


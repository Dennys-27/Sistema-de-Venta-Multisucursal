-- OBTENER REGISTRO POR ID
CREATE PROCEDURE SP_L_ROL_02
@ROL_ID INT
AS
BEGIN 
	SELECT * FROM [dbo].[TM_ROL]
	WHERE
	ROL_ID = @ROL_ID
END


-- ELIMINAR REGISTRO 
CREATE PROCEDURE SP_D_ROL_01
@ROL_ID INT
AS
BEGIN 
	UPDATE [dbo].[TM_ROL]
	SET
		EST = 0
	WHERE
		ROL_ID = @ROL_ID
END


-- INSERTAR REGISTRO
CREATE PROCEDURE SP_I_ROL_01
@SUC_ID INT,
@ROL_NOM VARCHAR(150)
AS
BEGIN 
	INSERT INTO [dbo].[TM_ROL]
	(SUC_ID,ROL_NOM,FECH_CREA,EST)
	VALUES
	(@SUC_ID,@ROL_NOM,GETDATE(),1)
END


-- ACTUALIZAR NUEVO REGISTRO
CREATE PROCEDURE SP_U_ROL_01
@ROL_ID INT,
@SUC_ID INT,
@ROL_NOM VARCHAR(150)
AS
BEGIN 
	UPDATE TM_ROL
	SET
		SUC_ID = @SUC_ID,
		ROL_NOM = @ROL_NOM
	WHERE
		ROL_ID = @ROL_ID
END


